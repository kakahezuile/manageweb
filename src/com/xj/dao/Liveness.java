package com.xj.dao;

public class Liveness {

	private Integer id;
	private Integer yesterdayInstalls;
	private Integer yesterday;
	private Integer lastWeekInstalls;
	private Integer lastWeek;
	private Integer communityId;
	public Integer getId() {
		return id;
	}
	public void setId(Integer id) {
		this.id = id;
	}
	public Integer getYesterday() {
		return yesterday;
	}
	public void setYesterday(Integer yesterday) {
		this.yesterday = yesterday;
	}
	public Integer getLastWeek() {
		return lastWeek;
	}
	public void setLastWeek(Integer lastWeek) {
		this.lastWeek = lastWeek;
	}
	public Integer getCommunityId() {
		return communityId;
	}
	public void setCommunityId(Integer communityId) {
		this.communityId = communityId;
	}
	public Integer getYesterdayInstalls() {
		return yesterdayInstalls;
	}
	public void setYesterdayInstalls(Integer yesterdayInstalls) {
		this.yesterdayInstalls = yesterdayInstalls;
	}
	public Integer getLastWeekInstalls() {
		return lastWeekInstalls;
	}
	public void setLastWeekInstalls(Integer lastWeekInstalls) {
		this.lastWeekInstalls = lastWeekInstalls;
	}
	
	
}
