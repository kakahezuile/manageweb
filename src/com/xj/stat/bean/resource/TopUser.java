package com.xj.stat.bean.resource;

public class TopUser {

	private String total;
	private String register;
	private String thisUser;
	private String totalLiveness;
	private String registerLiveness;
	public String getTotal() {
		return total;
	}
	public void setTotal(String total) {
		this.total = total;
	}
	public String getRegister() {
		return register;
	}
	public void setRegister(String register) {
		this.register = register;
	}
	public String getThisUser() {
		return thisUser;
	}
	public void setThisUser(String thisUser) {
		this.thisUser = thisUser;
	}
	public String getTotalLiveness() {
		return totalLiveness;
	}
	public void setTotalLiveness(String totalLiveness) {
		this.totalLiveness = totalLiveness;
	}
	public String getRegisterLiveness() {
		return registerLiveness;
	}
	public void setRegisterLiveness(String registerLiveness) {
		this.registerLiveness = registerLiveness;
	}
	
	
}
