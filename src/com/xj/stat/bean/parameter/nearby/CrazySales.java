package com.xj.stat.bean.parameter.nearby;

public class CrazySales {

	private Integer crazySalesId;
	
	private String title;

	private String descr;

	private Integer pageNum;
	
	private Integer pageSize;
	
	private Integer communityId;
	
	public String getTitle() {
		return title;
	}

	public void setTitle(String title) {
		this.title = title;
	}

	public String getDescr() {
		return descr;
	}

	public void setDescr(String descr) {
		this.descr = descr;
	}

	public Integer getCrazySalesId() {
		return crazySalesId;
	}

	public void setCrazySalesId(Integer crazySalesId) {
		this.crazySalesId = crazySalesId;
	}

	public Integer getCommunityId() {
		return communityId;
	}

	public void setCommunityId(Integer communityId) {
		this.communityId = communityId;
	}

	public Integer getPageNum() {
		return pageNum;
	}

	public void setPageNum(Integer pageNum) {
		this.pageNum = pageNum;
	}

	public Integer getPageSize() {
		return pageSize;
	}

	public void setPageSize(Integer pageSize) {
		this.pageSize = pageSize;
	}

	
	
}
