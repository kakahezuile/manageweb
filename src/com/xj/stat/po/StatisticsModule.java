package com.xj.stat.po;

public class StatisticsModule {

	private Integer statisticsModuleId;
	private String emobId;
	private Integer click;
	private Integer type;
	private Integer statisticsTime;
	private Integer communityId;
	public Integer getStatisticsModuleId() {
		return statisticsModuleId;
	}
	public void setStatisticsModuleId(Integer statisticsModuleId) {
		this.statisticsModuleId = statisticsModuleId;
	}
	public String getEmobId() {
		return emobId;
	}
	public void setEmobId(String emobId) {
		this.emobId = emobId;
	}
	public Integer getClick() {
		return click;
	}
	public void setClick(Integer click) {
		this.click = click;
	}
	public Integer getType() {
		return type;
	}
	public void setType(Integer type) {
		this.type = type;
	}
	public Integer getStatisticsTime() {
		return statisticsTime;
	}
	public void setStatisticsTime(Integer statisticsTime) {
		this.statisticsTime = statisticsTime;
	}
	public Integer getCommunityId() {
		return communityId;
	}
	public void setCommunityId(Integer communityId) {
		this.communityId = communityId;
	}
	
	
	
}
