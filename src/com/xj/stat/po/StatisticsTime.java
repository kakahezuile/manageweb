package com.xj.stat.po;

public class StatisticsTime {

	private Integer statisticsTimeId;
	private Integer statisticsUserTime;
	private String type;
	
	public Integer getStatisticsTimeId() {
		return statisticsTimeId;
	}
	public void setStatisticsTimeId(Integer statisticsTimeId) {
		this.statisticsTimeId = statisticsTimeId;
	}
	public Integer getStatisticsUserTime() {
		return statisticsUserTime;
	}
	public void setStatisticsUserTime(Integer statisticsUserTime) {
		this.statisticsUserTime = statisticsUserTime;
	}
	public String getType() {
		return type;
	}
	public void setType(String type) {
		this.type = type;
	}
	
	
	
}
