package com.xj.bean;

public class ActivitiesDate {
	private Integer activitiesId;
	private Integer activitesNum;
	private Integer addActivity;
    private Integer activityTime;
    private String activityTitle;
    private String emobGroupId;
	public Integer getActivitesNum() {
		return activitesNum;
	}
	public void setActivitesNum(Integer activitesNum) {
		this.activitesNum = activitesNum;
	}
	public Integer getAddActivity() {
		return addActivity;
	}
	public void setAddActivity(Integer addActivity) {
		this.addActivity = addActivity;
	}
	public Integer getActivityTime() {
		return activityTime;
	}
	public void setActivityTime(Integer activityTime) {
		this.activityTime = activityTime;
	}
	public String getActivityTitle() {
		return activityTitle;
	}
	public void setActivityTitle(String activityTitle) {
		this.activityTitle = activityTitle;
	}
	public Integer getActivitiesId() {
		return activitiesId;
	}
	public void setActivitiesId(Integer activitiesId) {
		this.activitiesId = activitiesId;
	}
	public String getEmobGroupId() {
		return emobGroupId;
	}
	public void setEmobGroupId(String emobGroupId) {
		this.emobGroupId = emobGroupId;
	}
	
    
}
