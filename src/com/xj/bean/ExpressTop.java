package com.xj.bean;

public class ExpressTop {

	private Integer expressTime;
	private Integer expressNum;
	private Integer appMessage;
	private Integer shortNote;
	
	
	public Integer getExpressNum() {
		return expressNum;
	}
	public void setExpressNum(Integer expressNum) {
		this.expressNum = expressNum;
	}
	public Integer getAppMessage() {
		return appMessage;
	}
	public void setAppMessage(Integer appMessage) {
		this.appMessage = appMessage;
	}
	public Integer getShortNote() {
		return shortNote;
	}
	public void setShortNote(Integer shortNote) {
		this.shortNote = shortNote;
	}
	public Integer getExpressTime() {
		return expressTime;
	}
	public void setExpressTime(Integer expressTime) {
		this.expressTime = expressTime;
	}
	
	
}
