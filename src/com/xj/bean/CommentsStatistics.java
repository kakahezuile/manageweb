package com.xj.bean;

public class CommentsStatistics {

	private Integer badComments;
	private Integer commentsNum;
	private Integer shopId;
	private Integer complaintsNum;
	private String shopName;
	private String score;
	public Integer getBadComments() {
		return badComments;
	}
	public void setBadComments(Integer badComments) {
		this.badComments = badComments;
	}
	public Integer getCommentsNum() {
		return commentsNum;
	}
	public void setCommentsNum(Integer commentsNum) {
		this.commentsNum = commentsNum;
	}
	public Integer getShopId() {
		return shopId;
	}
	public void setShopId(Integer shopId) {
		this.shopId = shopId;
	}
	public Integer getComplaintsNum() {
		return complaintsNum;
	}
	public void setComplaintsNum(Integer complaintsNum) {
		this.complaintsNum = complaintsNum;
	}
	public String getShopName() {
		return shopName;
	}
	public void setShopName(String shopName) {
		this.shopName = shopName;
	}
	public String getScore() {
		return score;
	}
	public void setScore(String score) {
		this.score = score;
	}
	
	
}
