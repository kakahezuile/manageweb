package com.xj.bean;

public class ShopSupervision {

	private Integer orderNum;
	private String emobIdShop;
	private String emobIdUser;
	private String username;
	private String nickname;
	private String userFloor;
	private String userUnit;
	private String room;
	public String getEmobIdShop() {
		return emobIdShop;
	}
	public void setEmobIdShop(String emobIdShop) {
		this.emobIdShop = emobIdShop;
	}
	public String getEmobIdUser() {
		return emobIdUser;
	}
	public void setEmobIdUser(String emobIdUser) {
		this.emobIdUser = emobIdUser;
	}
	public String getUsername() {
		return username;
	}
	public void setUsername(String username) {
		this.username = username;
	}
	public String getUserFloor() {
		return userFloor;
	}
	public void setUserFloor(String userFloor) {
		this.userFloor = userFloor;
	}
	public String getUserUnit() {
		return userUnit;
	}
	public void setUserUnit(String userUnit) {
		this.userUnit = userUnit;
	}
	public String getRoom() {
		return room;
	}
	public void setRoom(String room) {
		this.room = room;
	}
	public String getNickname() {
		return nickname;
	}
	public void setNickname(String nickname) {
		this.nickname = nickname;
	}
	public Integer getOrderNum() {
		return orderNum;
	}
	public void setOrderNum(Integer orderNum) {
		this.orderNum = orderNum;
	}
	
}
