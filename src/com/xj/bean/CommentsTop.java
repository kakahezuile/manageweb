package com.xj.bean;


/**
 * 评论头部
 * @author longfei
 *
 */
public class CommentsTop {

	
	private Integer commentsNum;
	private Integer goodComments;
	private Integer ordersNum;
	private Integer complaintsNum;
	private Integer score;
	public Integer getCommentsNum() {
		return commentsNum;
	}
	public void setCommentsNum(Integer commentsNum) {
		this.commentsNum = commentsNum;
	}
	public Integer getGoodComments() {
		return goodComments;
	}
	public void setGoodComments(Integer goodComments) {
		this.goodComments = goodComments;
	}
	public Integer getOrdersNum() {
		return ordersNum;
	}
	public void setOrdersNum(Integer ordersNum) {
		this.ordersNum = ordersNum;
	}
	public Integer getComplaintsNum() {
		return complaintsNum;
	}
	public void setComplaintsNum(Integer complaintsNum) {
		this.complaintsNum = complaintsNum;
	}
	public Integer getScore() {
		return score;
	}
	public void setScore(Integer score) {
		this.score = score;
	}
	
	
}
