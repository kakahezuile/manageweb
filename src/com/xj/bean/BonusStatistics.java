package com.xj.bean;

/**
 * 帮帮卷统计
 * @author longfei
 *
 */
public class BonusStatistics {

	private Integer bonusId;
	private String bonusName;
	private String bonusDetail;
	private Integer bonusNum; 
	private Integer bonusParSum;
	private Integer bonusPar;
	private Integer createTime;
	private Integer bonusType;
	
	
	public Integer getBonusId() {
		return bonusId;
	}
	public void setBonusId(Integer bonusId) {
		this.bonusId = bonusId;
	}
	public String getBonusName() {
		return bonusName;
	}
	public void setBonusName(String bonusName) {
		this.bonusName = bonusName;
	}
	public String getBonusDetail() {
		return bonusDetail;
	}
	public void setBonusDetail(String bonusDetail) {
		this.bonusDetail = bonusDetail;
	}
	public Integer getBonusNum() {
		return bonusNum;
	}
	public void setBonusNum(Integer bonusNum) {
		this.bonusNum = bonusNum;
	}
	public Integer getBonusParSum() {
		return bonusParSum;
	}
	public void setBonusParSum(Integer bonusParSum) {
		this.bonusParSum = bonusParSum;
	}
	public Integer getCreateTime() {
		return createTime;
	}
	public void setCreateTime(Integer createTime) {
		this.createTime = createTime;
	}
	public Integer getBonusType() {
		return bonusType;
	}
	public void setBonusType(Integer bonusType) {
		this.bonusType = bonusType;
	}
	public Integer getBonusPar() {
		return bonusPar;
	}
	public void setBonusPar(Integer bonusPar) {
		this.bonusPar = bonusPar;
	}
	
	
}
