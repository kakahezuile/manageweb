package com.xj.bean;

public class CommunityShops {

	private Integer communityShopsId;
	private Integer communityId;
	private Integer shopId;
	private Integer createTime;
	
	
	public Integer getCommunityShopsId() {
		return communityShopsId;
	}
	public void setCommunityShopsId(Integer communityShopsId) {
		this.communityShopsId = communityShopsId;
	}
	public Integer getCommunityId() {
		return communityId;
	}
	public void setCommunityId(Integer communityId) {
		this.communityId = communityId;
	}
	public Integer getShopId() {
		return shopId;
	}
	public void setShopId(Integer shopId) {
		this.shopId = shopId;
	}
	public Integer getCreateTime() {
		return createTime;
	}
	public void setCreateTime(Integer createTime) {
		this.createTime = createTime;
	}
	
	
	
}
