package com.xj.bean.click;

public class StatisticsUser {

	private Integer statisticsUserId;
	private String emobId;
	private Integer statisticsTime;
	private String type;
	private Integer clickNum;
	private Integer communityId;
	public Integer getStatisticsUserId() {
		return statisticsUserId;
	}
	public void setStatisticsUserId(Integer statisticsUserId) {
		this.statisticsUserId = statisticsUserId;
	}
	public String getEmobId() {
		return emobId;
	}
	public void setEmobId(String emobId) {
		this.emobId = emobId;
	}
	public Integer getStatisticsTime() {
		return statisticsTime;
	}
	public void setStatisticsTime(Integer statisticsTime) {
		this.statisticsTime = statisticsTime;
	}
	public String getType() {
		return type;
	}
	public void setType(String type) {
		this.type = type;
	}
	public Integer getClickNum() {
		return clickNum;
	}
	public void setClickNum(Integer clickNum) {
		this.clickNum = clickNum;
	}
	public Integer getCommunityId() {
		return communityId;
	}
	public void setCommunityId(Integer communityId) {
		this.communityId = communityId;
	}
	
	
	
	
}
