package com.xj.bean;

public class ComplaintsDetail {
	
	private Integer userId; // 用户id
	
	private String nickname; // 昵称
	
	private String username; // 昵称
	
	private String avatar; // 用户表中的 头像地址
	
	private String phone; //　联系电话
	
	private String userUnit; // 单元
	
	private String userFloor; // 楼栋
	
	private String room; // 房间号
	
    private String detail;
    
    private String deductionDetail;
    
	public Integer getUserId() {
		return userId;
	}

	public void setUserId(Integer userId) {
		this.userId = userId;
	}

	public String getNickname() {
		return nickname;
	}

	public void setNickname(String nickname) {
		this.nickname = nickname;
	}

	public String getUsername() {
		return username;
	}

	public void setUsername(String username) {
		this.username = username;
	}

	public String getAvatar() {
		return avatar;
	}

	public void setAvatar(String avatar) {
		this.avatar = avatar;
	}

	public String getPhone() {
		return phone;
	}

	public void setPhone(String phone) {
		this.phone = phone;
	}

	public String getUserUnit() {
		return userUnit;
	}

	public void setUserUnit(String userUnit) {
		this.userUnit = userUnit;
	}

	public String getUserFloor() {
		return userFloor;
	}

	public void setUserFloor(String userFloor) {
		this.userFloor = userFloor;
	}

	public String getRoom() {
		return room;
	}

	public void setRoom(String room) {
		this.room = room;
	}

	public String getDetail() {
		return detail;
	}

	public void setDetail(String detail) {
		this.detail = detail;
	}

	public String getDeductionDetail() {
		return deductionDetail;
	}

	public void setDeductionDetail(String deductionDetail) {
		this.deductionDetail = deductionDetail;
	}

	
	
}
